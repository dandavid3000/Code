     public boolean Send(String From,String To,String subject,String body)
        {
                String sentMessage = "";
                String receivedMessage = "";
              
                try {
                Socket s = new Socket(Login.SvAd, 25);
                BufferedReader br = new BufferedReader(new InputStreamReader(s.getInputStream(), "UTF8"));
                BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(s.getOutputStream(),"UTF8"));

                sentMessage="Helo khtn.edu";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                receivedMessage=br.readLine();

                sentMessage="";
                sentMessage="auth login";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();

                sentMessage="";
                sentMessage=Login.User;
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                sentMessage="";

                sentMessage=Login.PW;
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                sentMessage="";
                sentMessage="MAIL FROM: "+From+"";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                sentMessage="";
                sentMessage="RCPT TO: "+To+"";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                sentMessage="";
                sentMessage="Data";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();

                //Lay ngay thang nam
                
                Date now = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("dd MM yyyy");
                String date = sdf.format(now);
                

                sentMessage="";
                sentMessage= "Date: "+ date +"\r\n" + "From: " + From + "\r\n" +"To: " + To + "\r\n" + "Subject: "+ subject + "\r\n\r\n" + body +"\r\n.\r\n";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();
                         
                bw.write("QUIT");
                bw.newLine();
                bw.flush();
                receivedMessage=br.readLine();

                sentMessage="";
                sentMessage="Quit";
                bw.write(sentMessage);
                bw.newLine();
                bw.flush();
                
                br.close();
                bw.close();
                s.close();

                String []ST= receivedMessage.split(" ");
                if(ST[0].equals("+See"))
                  return true;
                else return false;
            }
                catch (IOException e)
                {JOptionPane.showMessageDialog(new JFrame(), "Chức năng bị lỗi", "Lỗi gửi mail", JOptionPane.ERROR_MESSAGE);
                   e.printStackTrace();
                   System.out.println("Lỗi gửi mail : " + e.getMessage());
            }
                // note cho nay
            

                return true;
    }

