    private void LoadBangPhieuThu()
    {
        RenewTablePhieuThu();
        database dan = new database();
        dan.DuLieu=dan.ConnectDatabase("select * from PhieuThu");
        int i =0;
        while(i<dan.n)
        {
            tbPhieuThu.setValueAt(dan.DuLieu[i][0], i, 0);
            tbPhieuThu.setValueAt(dan.DuLieu[i][1], i, 1);
            tbPhieuThu.setValueAt(dan.DuLieu[i][2], i, 2);
            tbPhieuThu.setValueAt(dan.DuLieu[i][3], i, 3);
            tbPhieuThu.setValueAt(dan.DuLieu[i][4], i, 4);
            i++;
         }

    }
